package com.revature.b;

public class PrimitivesDriver {
	public static void main(String[] args) {
		boolean b = true;
		System.out.println("b is: " + b);
		
		byte by = 5;
		System.out.println("by = " + by);
		
		char c = 102;
		System.out.println("c = " + c);
		c = 'k';
		System.out.println("now c = " + c);
		
		short s = 500;
		System.out.println("s = " + s);
		
		int i = 1293213980;
		System.out.println("i = " + i);
		
		// this is a comment it is for ducumentation it is not actually run
		long l = 2384793298473298l; // l is needed for long literals
		System.out.println("l = " + l);
		
		float f = 213891273.25f; // by default all decimal literals are double
		System.out.println("f = " + f);
		
		double dub = 328947329.328472397;
		System.out.println("dub = " + dub);
	}
}
